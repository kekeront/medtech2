_PATTERN = re.compile(
    r"(?<![а-яёa-z0-9])(" + "|".join(re.escape(k) for k in _KEYS) + r")(?![а-яёa-z0-9])"
)


def expand_abbreviations(text: str) -> str:
    """Replace whole-word medical abbreviations with their full forms.

    Expects already-normalized (lower-case, ё→е) input — see ruslang.normalize_ru.
    """
    if not text:
        return text
    return _PATTERN.sub(lambda m: MED_ABBREVIATIONS[m.group(1)], text)
